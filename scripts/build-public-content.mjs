#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = resolve(root, "config/public-source-coverage.json");
const outputPath = resolve(root, "website/app/generated-public-coverage.ts");
const brandPath = resolve(root, "config/ross-brand.json");
const brandOutputPath = resolve(root, "website/app/generated-brand-config.ts");
const check = process.argv.includes("--check");
const source = JSON.parse(readFileSync(sourcePath, "utf8"));

if (!/^\d{4}-\d{2}-\d{2}$/.test(source.asOfDate)) {
  throw new Error("Public coverage asOfDate must use YYYY-MM-DD.");
}
if (!Array.isArray(source.providers) || source.providers.length < 1) {
  throw new Error("Public coverage must contain at least one provider.");
}
const ids = new Set();
for (const provider of source.providers) {
  if (ids.has(provider.id))
    throw new Error(`Duplicate provider: ${provider.id}`);
  ids.add(provider.id);
  const implementationPath = resolve(root, provider.implementationSource);
  const implementation = readFileSync(implementationPath, "utf8");
  if (!implementation.includes(`id: "${provider.id}"`)) {
    throw new Error(
      `${provider.id} is not declared in ${provider.implementationSource}.`,
    );
  }
  if (!provider.knownLimits || !provider.integrationStatus) {
    throw new Error(`${provider.id} is missing public status or limits.`);
  }
}

const serialized = `${JSON.stringify(source, null, 2)} as const`;
const output = `// Generated by scripts/build-public-content.mjs. Do not edit directly.\n\nexport const PUBLIC_SOURCE_COVERAGE = ${serialized};\n`;
const brand = JSON.parse(readFileSync(brandPath, "utf8"));
const publicBrand = {
  product: brand.product,
  urls: brand.urls,
  policy: brand.policy,
  attribution: brand.attribution,
  socialLinks: brand.socialLinks,
};
const brandOutput = `// Generated by scripts/build-public-content.mjs. Do not edit directly.\n\nexport const PUBLIC_BRAND_CONFIG = ${JSON.stringify(publicBrand, null, 2)} as const;\n`;

function verifyOrWrite(path, contents, label) {
  if (check) {
    if (readFileSync(path, "utf8") !== contents) {
      throw new Error(
        `Generated ${label} is stale. Run npm run build:public-content.`,
      );
    }
  } else {
    writeFileSync(path, contents);
  }
}

verifyOrWrite(outputPath, output, "public coverage");
verifyOrWrite(brandOutputPath, brandOutput, "public brand config");
